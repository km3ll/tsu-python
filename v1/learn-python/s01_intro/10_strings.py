# strings
single_quotes = "Hello"
print(single_quotes)

double_quotes = 'Pod said: "Hello"'
print(double_quotes)

escaping_quotes = "Pod said: \"Hello\""
print(escaping_quotes)

multiline = """Hello.
My name is Pod042.
"""
print(multiline)

