# 2. numbers
print("----------")
# integers & floating-point numbers
age = 35
print("integer number: ", age)
PI = 3.14159
print("float number: ", PI)
math_operation = 1 + 3 * 4 / 2 - 2
print("math operation: ", math_operation)

# Whenever you have a division you'll get a float type
float_division = 8 / 3
print("float division: 8 / 3 = ", float_division)

# Integer division does not round the numbers up or down.
# It just removes everything after the decimal place
integer_division = 8 // 3
print("integer division: 8 // 3 = ", integer_division)

